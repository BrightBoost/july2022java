public class Engine {
    private int hp;
    private int nrOfCylinders;

    public int getHp() {
        return hp;
    }

    public void setHp(int hp) {
        this.hp = hp;
    }

    public int getNrOfCylinders() {
        return nrOfCylinders;
    }

    public void setNrOfCylinders(int nrOfCylinders) {
        this.nrOfCylinders = nrOfCylinders;
    }
}
